function generate_random_name($category_slug) {
    global $names_theme_words; // Ensure this global variable is accessible
    global $adjectives_words;

    $words = isset($names_theme_words[$category_slug]) ? $names_theme_words[$category_slug] : [];
    
    // Fallback in case there are no words for the category or it's not found
    if (empty($words)) {
        return "Default Name";
    }
    
    shuffle($words);
    $noun = $words[0];

    shuffle($adjectives_words);
    $adjective = $adjectives_words[0];

    return $adjective . ' ' . $noun; // Corrected concatenation operator
}

function generate_random_description() {
    $words = ['The', 'quick', 'brown', 'fox'];
    shuffle($words);
    return implode(' ', $words) . '.';
}



